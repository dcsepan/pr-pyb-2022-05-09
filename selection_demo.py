# Elágazások
name = "Jack Doe"

if name == "John Doe":  # Fej: if(kulcsszó), logikai kifejezés , végén : // Ha True lefutatja a törzset, ha false a törzs utáni első utasítás jön
    print("Hello John Doe!") # Törzs
elif name == "Jack Doe": #Több ág is lehetséges az elif-ből
    print("Ahoj Jack!")
else: #Egy lehetséges az else-ből
    print("Helló, nem ismerlek!")
    print("Kár")
print("end")

